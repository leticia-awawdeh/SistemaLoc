    private void btnCadEquipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadEquipActionPerformed
        String nomeEquip = txtNomeEquip.getText();
    String descricaoEquip = txtDescricaoEquip.getText();

    try {
        // Remove pontos e substitui vírgula por ponto para conversão numérica
        String valorDigitado = txtValorDiaria.getText().replace(".", "").replace(",", ".");
        double valorDiaria = Double.parseDouble(valorDigitado);

        // Lógica de cadastro e exibição
        Equipamento equipamento = new Equipamento(nomeEquip, descricaoEquip, valorDiaria);
        GerenciadorDados.adicionarEquipamento(equipamento);

        // Formata o valor com Utils
        String valorFormatado = Utils.formatarMonetario(valorDiaria);

        JOptionPane.showMessageDialog(panelEquip, "Equipamento registrado com sucesso: \n" +
                "Nome: " + nomeEquip + "\n" +
                "Descrição: " + descricaoEquip + "\n" +
                "Valor de Locação Diária: " + valorFormatado);

        // Limpa os campos
        txtNomeEquip.setText("");
        txtDescricaoEquip.setText("");
        txtValorDiaria.setText("");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(panelEquip, "Por favor, insira um valor válido no campo de locação diária.",
                "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCadEquipActionPerformed

